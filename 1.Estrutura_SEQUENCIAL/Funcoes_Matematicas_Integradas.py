# Encontrar número máximo e número mínimo
x = (1, 15, 31)
numeroMaximo = max(x)
print(f'Valor do maior número de {x} é = {numeroMaximo}')
numeroMinimo = min(x)
print(f'Valor do menor número de {x} é = {numeroMinimo}')

# Retorna valor absoluto (positivo) do número
y = -520
valorPositivo = abs(y)
print(f'O valor positivo do numero {y} é = {valorPositivo}')

# Calcula valor de um número elevado potencia de outro
a = 5
b = 3
elevadoPotencia = pow(a,b)
print(f'Valor de {a} elevado a potência de {b} é = {elevadoPotencia}')
