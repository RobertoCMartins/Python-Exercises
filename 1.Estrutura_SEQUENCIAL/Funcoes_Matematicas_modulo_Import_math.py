import math

# Método que calcula Raiz Quadrada
a = float(input("Digite um número: "))
raizQuadrada = math.sqrt(a)
print(f'A raiz quadrada de {a:.2f} é = {raizQuadrada:.2f}')


# Método que arredonda o número para cima
b = 10.5
arredondaCima = math.ceil(b)
print(f'Número {b} arredondado para cima é = {arredondaCima}')

# Método que arredonda o número para baixo
c = 10.5
arredondaCima = math.floor(c)
print(f'Número {c} arredondado para cima é = {arredondaCima}')

# Constante que retorna o número de PI
numeroPI = math.pi
print(f'Valor do PI é = {numeroPI}')